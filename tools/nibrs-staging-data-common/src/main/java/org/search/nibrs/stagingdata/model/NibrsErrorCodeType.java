package org.search.nibrs.stagingdata.model;

import javax.persistence.Cacheable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

import org.apache.commons.lang3.builder.ToStringBuilder;

@Entity
@Cacheable
public class NibrsErrorCodeType {
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private Integer nibrsErrorCodeTypeId; 
	
	private String code; 
	private String type; 
	private String message; 
	private String description; 
	
	public NibrsErrorCodeType() {
		super();
	}

	public NibrsErrorCodeType(Integer nibrsErrorCodeTypeId) {
		super();
		this.setNibrsErrorCodeTypeId(nibrsErrorCodeTypeId);
	}


	public String toString(){
		return ToStringBuilder.reflectionToString(this);
	}

	public Integer getNibrsErrorCodeTypeId() {
		return nibrsErrorCodeTypeId;
	}

	public void setNibrsErrorCodeTypeId(Integer nibrsErrorCodeTypeId) {
		this.nibrsErrorCodeTypeId = nibrsErrorCodeTypeId;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((code == null) ? 0 : code.hashCode());
		result = prime * result + ((description == null) ? 0 : description.hashCode());
		result = prime * result + ((message == null) ? 0 : message.hashCode());
		result = prime * result + ((nibrsErrorCodeTypeId == null) ? 0 : nibrsErrorCodeTypeId.hashCode());
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		NibrsErrorCodeType other = (NibrsErrorCodeType) obj;
		if (code == null) {
			if (other.code != null)
				return false;
		} else if (!code.equals(other.code))
			return false;
		if (description == null) {
			if (other.description != null)
				return false;
		} else if (!description.equals(other.description))
			return false;
		if (message == null) {
			if (other.message != null)
				return false;
		} else if (!message.equals(other.message))
			return false;
		if (nibrsErrorCodeTypeId == null) {
			if (other.nibrsErrorCodeTypeId != null)
				return false;
		} else if (!nibrsErrorCodeTypeId.equals(other.nibrsErrorCodeTypeId))
			return false;
		if (type == null) {
			if (other.type != null)
				return false;
		} else if (!type.equals(other.type))
			return false;
		return true;
	}


}
